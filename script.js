function initFAQ() {
    const faqItems = document.querySelectorAll('.faq-item');
    faqItems.forEach(item => {
        const question = item.querySelector('.faq-question');
        question.addEventListener('click', () => {
            const isActive = item.classList.contains('active');
            // Close all other items
            faqItems.forEach(i => i.classList.remove('active'));
            if (!isActive) {
                item.classList.add('active');
            }
        });
    });
}

function initTheme() {
    const toggle = document.getElementById('theme-toggle');
    const body = document.body;
    
    // Theme is partially handled by inline script, but let's ensure full sync
    const savedTheme = localStorage.getItem('sarkari-theme') || 'light-theme';
    body.className = savedTheme;
    updateToggleIcon(body, toggle);

    toggle.addEventListener('click', () => {
        if (body.classList.contains('light-theme')) {
            body.classList.replace('light-theme', 'dark-theme');
            localStorage.setItem('sarkari-theme', 'dark-theme');
        } else {
            body.classList.replace('dark-theme', 'light-theme');
            localStorage.setItem('sarkari-theme', 'light-theme');
        }
        updateToggleIcon(body, toggle);
    });
}

function updateToggleIcon(body, toggle) {
    const icon = toggle.querySelector('i');
    if (body.classList.contains('light-theme')) {
        icon.className = 'ph ph-sun';
    } else {
        icon.className = 'ph ph-moon';
    }
}

function initMobileMenu() {
    const mobileBtn = document.getElementById('mobile-menu-btn');
    const desktopNav = document.querySelector('.desktop-nav');
    
    if (mobileBtn && desktopNav) {
        mobileBtn.addEventListener('click', () => {
            desktopNav.classList.toggle('active');
            
            // Toggle icon
            const icon = mobileBtn.querySelector('i');
            if (desktopNav.classList.contains('active')) {
                icon.className = 'ph ph-x';
            } else {
                icon.className = 'ph ph-list';
            }
        });
        
        // Close menu when clicking a link
        const navLinks = desktopNav.querySelectorAll('a');
        navLinks.forEach(link => {
            link.addEventListener('click', () => {
                desktopNav.classList.remove('active');
                const icon = mobileBtn.querySelector('i');
                if(icon) icon.className = 'ph ph-list';
            });
        });
    }
}

function initNavigation() {
    // Scroll spy for active navy link
    const sections = document.querySelectorAll('section[id], main[id]');
    const navLinks = document.querySelectorAll('.desktop-nav a:not(.more-link)');

    window.addEventListener('scroll', () => {
        let current = '';
        const scrollY = window.pageYOffset;
        
        sections.forEach(section => {
            const sectionTop = section.offsetTop;
            const sectionHeight = section.clientHeight;
            if (scrollY >= (sectionTop - 150)) {
                current = section.getAttribute('id');
            }
        });

        navLinks.forEach(link => {
            link.classList.remove('active');
            if (link.getAttribute('href') === `#${current}`) {
                link.classList.add('active');
            }
        });
    });
}

function populateUI() {
    // Populate Trending
    const heroGrid = document.getElementById('hero-grid');
    if (heroGrid) {
        heroGrid.innerHTML = '';
        data.trending.forEach(item => {
            const card = document.createElement('div');
            card.className = 'hero-card';
            const id = slugify(item.title);
            card.innerHTML = `
                <a href="links/${id}.html" style="text-decoration:none; color:inherit; display:block; width:100%; height:100%;">
                    <h4>${item.title}</h4>
                    <span class="tag">${item.tag}</span>
                </a>
            `;
            heroGrid.appendChild(card);
        });
    }

    // Populate Lists with limit 15 for homepage
    renderList('results-list', data.results, 15, false);
    renderList('admit-card-list', data.admitCard, 15, false);
    renderList('latest-jobs-list', data.latestJobs, 15, true);
    renderList('answer-key-list', data.answerKey, 15, false);
    renderList('syllabus-list', data.syllabus, 15, false);
    renderList('admission-list', data.admission, 15, false);
    renderList('documents-list', data.documents, 15, false);
}

function slugify(text) {
    return text.toLowerCase()
        .replace(/ /g, '-')
        .replace(/\//g, '')
        .replace(/[()]/g, '')
        .replace(/&/g, '')
        .replace(/–/g, '-')
        .replace(/--+/g, '-')
        .replace(/^-+|-+$/g, '');
}

function initSearch() {
    const searchInput = document.querySelector('.search-box input');
    
    if (searchInput) {
        searchInput.addEventListener('input', (e) => {
            const query = e.target.value.toLowerCase();
            const allItems = document.querySelectorAll('.link-item, .hero-card');
            
            allItems.forEach(item => {
                const text = item.innerText.toLowerCase();
                if (text.includes(query)) {
                    item.style.display = 'block';
                } else {
                    item.style.display = 'none';
                }
            });
        });
    }
}

// Modify renderList to take a limit parameter and showDate flag
function renderList(elementId, items, limit = null, showDate = false) {
    const list = document.getElementById(elementId);
    if (!list || !items) return;
    
    list.innerHTML = '';
    const displayItems = limit ? items.slice(0, limit) : items;
    
    displayItems.forEach((item, index) => {
        const li = document.createElement('li');
        li.className = `link-item ${item.isNew ? 'new' : ''}`;
        
        const id = slugify(item.title);
        const url = `links/${id}.html`;
        
        let lastDateHtml = '';
        if (showDate) {
            const lastDateText = item.lastDate ? `Last Date: ${item.lastDate}` : 'Coming Soon';
            lastDateHtml = `<span class="last-date">${lastDateText}</span>`;
        }
        
        li.innerHTML = `
            <a href="${url}" class="job-title">${item.title}</a>
            ${lastDateHtml}
        `;
        list.appendChild(li);
    });
}

// Dedicated Category Page Logic
function populateCategoryPage() {
    const urlParams = new URLSearchParams(window.location.search);
    const catType = urlParams.get('type');
    
    if (catType && data[catType]) {
        // Set page title and header
        const pageTitle = document.getElementById('category-page-title');
        if (pageTitle) {
            const formattedTitle = catType.replace(/([A-Z])/g, ' $1').replace(/^./, str => str.toUpperCase());
            pageTitle.innerHTML = `<i class="ph-bold ph-list-dashes"></i> ${formattedTitle} Category`;
            document.title = `${formattedTitle} - Sarkari Result Premium`;
        }
        
        // Render unlimited items
        renderList('category-list', data[catType], null, catType === 'latestJobs');
    } else {
        const list = document.getElementById('category-list');
        if (list) list.innerHTML = '<li><a href="#">Error loading category data.</a></li>';
    }
}

document.addEventListener('DOMContentLoaded', () => {
    initTheme();
    
    if (document.getElementById('home') || document.getElementById('results-list')) {
        populateUI();
    } else if (document.getElementById('category-list')) {
        populateCategoryPage();
    }
    
    initSearch();
    initMobileMenu();
    initNavigation();
    if(typeof initFAQ === 'function') initFAQ();
});
