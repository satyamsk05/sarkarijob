function slugify(text) {
    return text.toLowerCase()
        .replace(/ /g, '-')
        .replace(/\//g, '')
        .replace(/[()]/g, '')
        .replace(/&/g, '')
        .replace(/–/g, '-')
        .replace(/--+/g, '-')
        .replace(/^-+|-+$/g, '');
}

function initSearch() {
    const searchTrigger = document.querySelector('.search-box');
    const overlay = document.getElementById('search-overlay');
    const overlayInput = document.getElementById('overlay-search-input');
    const closeBtn = document.getElementById('close-search');
    const resultsContainer = document.getElementById('search-results-container');

    if (!searchTrigger || !overlay || !overlayInput || !closeBtn || !resultsContainer) return;

    // Open Overlay
    searchTrigger.addEventListener('click', (e) => {
        e.preventDefault();
        overlay.style.display = 'block';
        setTimeout(() => overlay.classList.add('active'), 10);
        overlayInput.focus();
        document.body.style.overflow = 'hidden'; // Prevent scrolling
    });

    // Close Overlay
    const closeOverlay = () => {
        overlay.classList.remove('active');
        setTimeout(() => overlay.style.display = 'none', 300);
        document.body.style.overflow = '';
        overlayInput.value = '';
        resetPlaceholder();
    };

    closeBtn.addEventListener('click', closeOverlay);

    // Close on Escape Key
    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && overlay.classList.contains('active')) {
            closeOverlay();
        }
    });

    // Perform Global Search
    overlayInput.addEventListener('input', (e) => {
        const query = e.target.value.toLowerCase().trim();
        
        if (query === "") {
            resetPlaceholder();
            return;
        }

        renderSearchResults(query);
    });

    function resetPlaceholder() {
        resultsContainer.innerHTML = `
            <div class="search-placeholder">
                <h3 class="suggestions-title"><i class="ph-bold ph-fire"></i> LATEST UPDATES</h3>
                <div id="suggestions-list" class="suggestions-wrapper"></div>
            </div>
        `;
        
        const suggestionsList = document.getElementById('suggestions-list');
        const latestItems = [];
        
        // Collect items marked as isNew
        for (const category in data) {
            data[category].forEach(item => {
                if (item.isNew && latestItems.length < 9) {
                    latestItems.push({ ...item, category });
                }
            });
        }

        latestItems.forEach(item => {
            const pill = document.createElement('a');
            pill.className = 'suggestion-pill';
            
            const id = slugify(item.title);
            const isInLinks = window.location.pathname.includes('/links/');
            const url = isInLinks ? `${id}.html` : `links/${id}.html`;
            pill.href = url;
            
            pill.innerHTML = `<i class="ph ph-sparkle"></i> ${item.title}`;
            suggestionsList.appendChild(pill);
        });
    }

    function renderSearchResults(query) {
        resultsContainer.innerHTML = '';
        let matchCount = 0;

        // Iterate through all categories in the data object
        for (const category in data) {
            const items = data[category];
            items.forEach(item => {
                if (item.title.toLowerCase().includes(query)) {
                    matchCount++;
                    const resultItem = document.createElement('a');
                    resultItem.className = 'overlay-result-item';
                    
                    const id = slugify(item.title);
                    const isInLinks = window.location.pathname.includes('/links/');
                    const url = isInLinks ? `${id}.html` : `links/${id}.html`;
                    resultItem.href = url;

                    resultItem.innerHTML = `
                        <div class="result-info">
                            <span class="result-tag">${category.replace(/([A-Z])/g, ' $1')}</span>
                            <h4>${item.title}</h4>
                        </div>
                        <i class="ph ph-arrow-right"></i>
                    `;
                    resultsContainer.appendChild(resultItem);
                }
            });
        }

        if (matchCount === 0) {
            resultsContainer.innerHTML = `
                <div class="search-placeholder">
                    <i class="ph ph-smiley-blank"></i>
                    <p>No results found for "${query}"</p>
                </div>
            `;
        }
    }
}

// Modify renderList to take a limit parameter and showDate flag
function renderList(elementId, items, limit = null, showDate = false) {
    const list = document.getElementById(elementId);
    if (!list || !items) return;
    
    list.innerHTML = '';
    const displayItems = limit ? items.slice(0, limit) : items;
    
    displayItems.forEach((item, index) => {
        const li = document.createElement('li');
        li.className = `link-item ${item.isNew ? 'new' : ''}`;
        
        const id = slugify(item.title);
        // Determine if we are in the links directory
        const isInLinks = window.location.pathname.includes('/links/');
        const url = isInLinks ? `${id}.html` : `links/${id}.html`;
        
        let lastDateHtml = '';
        if (showDate) {
            const lastDateText = item.lastDate ? `Last Date: ${item.lastDate}` : 'Coming Soon';
            lastDateHtml = `<span class="last-date">${lastDateText}</span>`;
        }
        
        li.innerHTML = `
            <a href="${url}" class="job-title">${item.title}</a>
            ${lastDateHtml}
        `;
        list.appendChild(li);
    });
}

// Dedicated Category Page Logic
function populateCategoryPage() {
    const urlParams = new URLSearchParams(window.location.search);
    const catType = urlParams.get('type');
    
    if (catType && data[catType]) {
        // Set page title and header
        const pageTitle = document.getElementById('category-page-title');
        if (pageTitle) {
            const formattedTitle = catType.replace(/([A-Z])/g, ' $1').replace(/^./, str => str.toUpperCase());
            pageTitle.innerHTML = `<i class="ph-bold ph-list-dashes"></i> ${formattedTitle} Category`;
            document.title = `${formattedTitle} - Sarkari Result Premium`;
        }
        
        // Render unlimited items
        renderList('category-list', data[catType], null, catType === 'latestJobs');
    } else {
        const list = document.getElementById('category-list');
        if (list) list.innerHTML = '<li><a href="#">Error loading category data.</a></li>';
    }
}

